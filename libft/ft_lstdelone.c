#include <stdlib.h>
#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}

/*
ft_lstdelone elimina el contenido de nodo mediante
la funcion del y lo libera con free
*/